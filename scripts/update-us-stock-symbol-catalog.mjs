import { mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const directoryUrl = new URL(".", import.meta.url);
const projectRoot = resolve(fileURLToPath(new URL("..", directoryUrl)));
const outputPath = resolve(projectRoot, "app/dashboard/us-stock-symbol-catalog.generated.ts");

const sources = [
  {
    exchange: () => "NASDAQ",
    url: "https://www.nasdaqtrader.com/dynamic/SymDir/nasdaqlisted.txt",
    symbolIndex: 0,
    nameIndex: 1,
    etfIndex: 6,
    testIssueIndex: 3,
  },
  {
    exchange: (parts) => ({
      A: "NYSE American",
      N: "NYSE",
      P: "NYSE Arca",
      Z: "Cboe BZX",
    })[parts[2]] ?? "Other U.S. exchange",
    url: "https://www.nasdaqtrader.com/dynamic/SymDir/otherlisted.txt",
    symbolIndex: 0,
    nameIndex: 1,
    etfIndex: 4,
    testIssueIndex: 6,
  },
];

const normalizeName = (value) => value
  .replace(/\s+-?\s*(?:Class [A-Z] )?(?:Common|Ordinary|Preferred) Stocks?\.?$/i, "")
  .replace(/\s+-?\s*(?:Class [A-Z] )?(?:Common|Ordinary|Preferred) Shares?\.?$/i, "")
  .replace(/\s+-\s+(?:American Depositary Shares?|ADS)\.?$/i, "")
  .replace(/\s+-\s+(?:Depositary Shares?|Shares)\.?$/i, "")
  .replace(/\s+/g, " ")
  .trim();

const escape = (value) => JSON.stringify(value);

const parseSource = (text, source) => text
  .trim()
  .split(/\r?\n/)
  .slice(1)
  .map((line) => line.split("|"))
  .filter((parts) => parts[source.testIssueIndex] === "N" && parts[source.etfIndex] === "N")
  .map((parts) => ({
    symbol: parts[source.symbolIndex]?.trim(),
    name: normalizeName(parts[source.nameIndex] ?? ""),
    exchange: source.exchange(parts),
  }))
  .filter((entry) => /^[A-Z][A-Z0-9.-]{0,11}$/.test(entry.symbol ?? "") && entry.name.length > 0);

const responses = await Promise.all(sources.map(async (source) => {
  const response = await fetch(source.url, {
    headers: { "user-agent": "FamilyStocks-Audit catalog generator" },
  });
  if (!response.ok) throw new Error(`Could not load ${source.url}: ${response.status}`);
  return { source, text: await response.text() };
}));

const entries = new Map();
for (const { source, text } of responses) {
  for (const entry of parseSource(text, source)) {
    if (!entries.has(entry.symbol)) entries.set(entry.symbol, entry);
  }
}

const catalog = [...entries.values()].sort((left, right) => left.symbol.localeCompare(right.symbol));
const generatedAt = new Date().toISOString();
const output = `/*\n * Generated by scripts/update-us-stock-symbol-catalog.mjs on ${generatedAt}.\n * Sources: Nasdaq Trader nasdaqlisted.txt and otherlisted.txt.\n * This is a client-side hint catalog only; Tiingo remains the source of analysis data.\n */\n\nexport type UsStockSymbolCatalogEntry = readonly [\n  symbol: string,\n  name: string,\n  exchange: string,\n];\n\nexport const US_STOCK_SYMBOL_CATALOG: readonly UsStockSymbolCatalogEntry[] = [\n${catalog
  .map((entry) => `  [${escape(entry.symbol)}, ${escape(entry.name)}, ${escape(entry.exchange)}],`)
  .join("\n")}\n];\n`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, output, "utf8");
console.log(`Wrote ${catalog.length.toLocaleString()} U.S. stock symbols to ${outputPath}`);
